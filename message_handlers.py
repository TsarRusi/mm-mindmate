async def show_stats(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """–ü–æ–∫–∞–∑–∞—Ç—å —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É"""
    user = update.effective_user
    
    # –ü–æ–ª—É—á–∞–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏–∑ –ë–î
    with db_manager.get_db_session() as session:
        from database import User
        db_user = session.query(User).filter(User.telegram_id == user.id).first()
        
        if not db_user:
            await update.message.reply_text("–í—ã –µ—â–µ –Ω–µ –Ω–∞—á–∞–ª–∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –±–æ—Ç–∞. –ù–∞–ø–∏—à–∏—Ç–µ /start")
            return
        
        stats = db_manager.get_user_stats(db_user.id)
        
        if stats['total_records'] == 0:
            await update.message.reply_text(
                "üìä *–°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞*\n\n"
                "–£ –≤–∞—Å –ø–æ–∫–∞ –Ω–µ—Ç –∑–∞–ø–∏—Å–µ–π –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∏—è.\n"
                "–ù–∞–ø–∏—à–∏—Ç–µ –º–Ω–µ, –∫–∞–∫ –≤—ã —Å–µ–±—è —á—É–≤—Å—Ç–≤—É–µ—Ç–µ, —á—Ç–æ–±—ã –Ω–∞—á–∞—Ç—å –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏–µ!",
                parse_mode='Markdown'
            )
            return
        
        # –§–æ—Ä–º–∏—Ä—É–µ–º —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É
        stat_text = f"""
        üìä *–í–∞—à–∞ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞*

        üìà –í—Å–µ–≥–æ –∑–∞–ø–∏—Å–µ–π: {stats['total_records']}
        üéØ –°—Ä–µ–¥–Ω–µ–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∏–µ: {stats['avg_mood']:.1f}/10
        
        üìù *–ü–æ—Å–ª–µ–¥–Ω–∏–µ –∑–∞–ø–∏—Å–∏:*
        """
        
        for log in stats.get('recent_logs', []):
            mood_emoji = "üòä" if log.get('mood_score', 5) >= 7 else "üòê" if log.get('mood_score', 5) >= 4 else "üòî"
            stat_text += f"\n{mood_emoji} {log.get('mood_score', '?')}/10: {log.get('message', '')}"
        
        stat_text += "\n\n*–ü—Ä–æ–¥–æ–ª–∂–∞–π—Ç–µ –æ—Ç—Å–ª–µ–∂–∏–≤–∞—Ç—å —Å–≤–æ—ë –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∏–µ!*"
        
        await update.message.reply_text(stat_text, parse_mode='Markdown')

